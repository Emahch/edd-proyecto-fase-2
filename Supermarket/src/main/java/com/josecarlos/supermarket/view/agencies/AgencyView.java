    /**
     * Muestra lista de productos disponibles para transferencia en un panel
     * existente
     */
    private void showProductsForTransfer(JPanel parentPanel) {
        SimpleProductsList products = vertex.getAgency().getCatalog().inorder();

        if (products.getHead() == null) {
            JLabel emptyLabel = new JLabel("No hay productos disponibles en esta sucursal");
            emptyLabel.setAlignmentX(Component.CENTER_ALIGNMENT);
            parentPanel.add(emptyLabel, BorderLayout.CENTER);
            return;
        }

        JPanel listPanel = new JPanel();
        listPanel.setLayout(new BoxLayout(listPanel, BoxLayout.Y_AXIS));
        listPanel.setBorder(javax.swing.BorderFactory.createTitledBorder("Productos Disponibles para Transferencia"));

        JScrollPane scrollPane = new JScrollPane(listPanel);
        Node<Product> head = products.getHead();
        while (head != null) {
            final Product product = head.getValue();
            JButton productBtn = new JButton(head.getValue().getName() + " - $" + head.getValue().getPrice());
            productBtn.setMaximumSize(new Dimension(Integer.MAX_VALUE, 50));
            productBtn.setAlignmentX(Component.CENTER_ALIGNMENT);

            productBtn.addActionListener(e -> {
                openTransferDialog(product);
            });

            listPanel.add(productBtn);
            listPanel.add(Box.createRigidArea(new Dimension(0, 5)));
            head = head.getNext();
        }

        parentPanel.add(scrollPane, BorderLayout.CENTER);
    }

    /**
     * Abre el diálogo para seleccionar destino del traslado
     */
    private void openTransferDialog(Product product) {
        java.awt.EventQueue.invokeLater(() -> {
            // Obtener el JFrame padre de manera segura
            JFrame parentFrame = (JFrame) javax.swing.SwingUtilities.getWindowAncestor(this);
            if (parentFrame == null) {
                parentFrame = new javax.swing.JFrame();
            }

            com.josecarlos.supermarket.view.agencies.TransferDestinationDialog dialog = new com.josecarlos.supermarket.view.agencies.TransferDestinationDialog(
                    parentFrame,
                    product,
                    vertex,
                    graph);
            dialog.setVisible(true);
        });
    }

