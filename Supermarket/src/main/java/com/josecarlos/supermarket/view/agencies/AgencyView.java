    private void diagramsActionPerformed(java.awt.event.ActionEvent evt) {
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Seleccione el directorio para guardar");

        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        fileChooser.setAcceptAllFileFilterUsed(false);

        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            String path = selectedFile.getAbsolutePath();

            vertex.getAgency().getCatalog().generateDiagrams(path);
        }
    }

    private void benchmarkActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_benchmarkActionPerformed
        JPanel listPanel = new JPanel();
        listPanel.setLayout(new BoxLayout(listPanel, BoxLayout.Y_AXIS));

        Benchmark benchmark = new Benchmark();
        List<BenchmarkResult> result = benchmark.runSearchBenchmark(
                benchmark.getRandomProducts(100, vertex.getAgency().getCatalog()), vertex.getAgency().getCatalog());
        JScrollPane scrollPane = new JScrollPane(listPanel);
        for (BenchmarkResult benchmarkResult : result) {

            JLabel productResult = new JLabel(benchmarkResult.toString());

            listPanel.add(productResult);
            listPanel.add(Box.createRigidArea(new Dimension(0, 5)));
        }
        remove(((BorderLayout) getLayout())
                .getLayoutComponent(BorderLayout.CENTER));
        add(scrollPane, BorderLayout.CENTER);
        revalidate();
        repaint();
    }// GEN-LAST:event_benchmarkActionPerformed

    @Override
    public void onProductsLoaded(SimpleProductsList products) {
        JPanel listPanel = new JPanel();
        listPanel.setLayout(new BoxLayout(listPanel, BoxLayout.Y_AXIS));

        JScrollPane scrollPane = new JScrollPane(listPanel);
        Node<Product> head = products.getHead();
        while (head != null) {
            final Product product = head.getValue();
            JButton productBtn = new JButton(head.getValue().getName() + " - $" + head.getValue().getPrice());
            productBtn.setMaximumSize(new Dimension(Integer.MAX_VALUE, 50));
            productBtn.setAlignmentX(Component.CENTER_ALIGNMENT);

            productBtn.addActionListener(e -> {
                handleProductSelection(product);
            });

            listPanel.add(productBtn);
            listPanel.add(Box.createRigidArea(new Dimension(0, 5)));
            head = head.getNext();
        }
        remove(((BorderLayout) getLayout())
                .getLayoutComponent(BorderLayout.CENTER));
        add(scrollPane, BorderLayout.CENTER);
        revalidate();
        repaint();
    }

    private void handleProductSelection(Product product) {
        java.awt.EventQueue.invokeLater(() -> {
            ProductActionsDialog dialog = new ProductActionsDialog(new javax.swing.JFrame(), true,
                    vertex.getAgency().getCatalog(), this, product);
            dialog.setVisible(true);
        });
    }

    @Override
    public void onVertexSelected(Vertex agency, ComparationMode mode) {
        Dijkstra dijkstra = new Dijkstra();
        Optional<PathResult> result = dijkstra.shortestPath(graph, vertex.getKey(), agency.getKey(), mode);
        if (result.isPresent()) {
            MovementService movementService = new MovementService(result.get(), agency);
            movementService.run();
        }
    }

    @Override
    public void onLoadAllProducts() {
        this.onProductsLoaded(vertex.getAgency().getCatalog().inorder());
    }

